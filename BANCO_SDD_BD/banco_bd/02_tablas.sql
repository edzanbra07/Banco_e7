  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  created_by BIGINT UNSIGNED NULL,
  updated_by BIGINT UNSIGNED NULL,
  PRIMARY KEY (id_transferencia),
  KEY idx_transferencia_origen (cuenta_origen_id),
  KEY idx_transferencia_destino (cuenta_destino_id),
  KEY idx_transferencia_estado (estado_transferencia_id),
  KEY idx_transferencia_fecha_creacion (fecha_creacion),
  KEY idx_transferencia_fecha_vencimiento (fecha_vencimiento),
