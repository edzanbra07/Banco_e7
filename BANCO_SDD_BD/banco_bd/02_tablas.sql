  CONSTRAINT fk_transferencia_estado FOREIGN KEY (estado_transferencia_id) REFERENCES cat_estado_transferencia (id_catalogo),
  CONSTRAINT fk_transferencia_creador FOREIGN KEY (id_usuario_creador) REFERENCES usuario_sistema (id_usuario),
  CONSTRAINT fk_transferencia_aprobador FOREIGN KEY (id_usuario_aprobador) REFERENCES usuario_sistema (id_usuario),
