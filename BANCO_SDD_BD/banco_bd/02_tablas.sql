  created_by BIGINT UNSIGNED NULL,
  updated_by BIGINT UNSIGNED NULL,
  PRIMARY KEY (id_cuenta),
  UNIQUE KEY uk_cuenta_numero_cuenta (numero_cuenta),
  KEY idx_cuenta_titular (id_titular_cliente),
