CREATE TABLE IF NOT EXISTS cuenta_movimiento (
  id_movimiento BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  id_cuenta BIGINT UNSIGNED NOT NULL,
  tipo_movimiento_id BIGINT UNSIGNED NOT NULL,
  monto DECIMAL(18,2) NOT NULL,
  saldo_anterior DECIMAL(18,2) NOT NULL,
  saldo_posterior DECIMAL(18,2) NOT NULL,
  referencia_tipo VARCHAR(60) NOT NULL,
  referencia_id VARCHAR(64) NOT NULL,
  descripcion VARCHAR(255) NOT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  created_by BIGINT UNSIGNED NULL,
  PRIMARY KEY (id_movimiento),
  KEY idx_movimiento_cuenta_fecha (id_cuenta, created_at),
  KEY idx_movimiento_referencia (referencia_tipo, referencia_id),
  CONSTRAINT fk_movimiento_cuenta FOREIGN KEY (id_cuenta) REFERENCES cuenta (id_cuenta),
  CONSTRAINT fk_movimiento_tipo FOREIGN KEY (tipo_movimiento_id) REFERENCES cat_tipo_movimiento_cuenta (id_catalogo),
  CONSTRAINT ck_movimiento_importes CHECK (monto > 0 AND saldo_anterior >= 0 AND saldo_posterior >= 0)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS prestamo (
  id_prestamo BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  tipo_prestamo_id BIGINT UNSIGNED NOT NULL,
  id_cliente_solicitante BIGINT UNSIGNED NOT NULL,
  monto_solicitado DECIMAL(18,2) NOT NULL,
  monto_aprobado DECIMAL(18,2) NULL,
  tasa_interes DECIMAL(9,4) NOT NULL,
  plazo_meses INT NOT NULL,
  estado_prestamo_id BIGINT UNSIGNED NOT NULL,
  fecha_solicitud DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  fecha_aprobacion DATETIME NULL,
  fecha_desembolso DATETIME NULL,
  id_cuenta_destino_desembolso BIGINT UNSIGNED NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  created_by BIGINT UNSIGNED NULL,
  updated_by BIGINT UNSIGNED NULL,
  PRIMARY KEY (id_prestamo),
  KEY idx_prestamo_cliente (id_cliente_solicitante),
  KEY idx_prestamo_estado (estado_prestamo_id),
  KEY idx_prestamo_fecha_solicitud (fecha_solicitud),
  KEY idx_prestamo_cuenta_desembolso (id_cuenta_destino_desembolso),
  CONSTRAINT fk_prestamo_tipo FOREIGN KEY (tipo_prestamo_id) REFERENCES cat_tipo_prestamo (id_catalogo),
  CONSTRAINT fk_prestamo_cliente FOREIGN KEY (id_cliente_solicitante) REFERENCES cliente (id_cliente),
  CONSTRAINT fk_prestamo_estado FOREIGN KEY (estado_prestamo_id) REFERENCES cat_estado_prestamo (id_catalogo),
  CONSTRAINT fk_prestamo_cuenta_desembolso FOREIGN KEY (id_cuenta_destino_desembolso) REFERENCES cuenta (id_cuenta),
  CONSTRAINT ck_prestamo_importes CHECK (monto_solicitado > 0 AND tasa_interes > 0 AND plazo_meses > 0 AND (monto_aprobado IS NULL OR monto_aprobado <= monto_solicitado))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

