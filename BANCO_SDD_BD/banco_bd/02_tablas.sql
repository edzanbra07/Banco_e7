USE banco_bd;

CREATE TABLE IF NOT EXISTS cliente (
  id_cliente BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  tipo_cliente_id BIGINT UNSIGNED NOT NULL,
  estado_cliente_id BIGINT UNSIGNED NOT NULL,
  id_identificacion VARCHAR(50) NOT NULL,
  nombre_completo VARCHAR(200) NOT NULL,
  correo_electronico VARCHAR(255) NOT NULL,
  telefono VARCHAR(20) NOT NULL,
  direccion VARCHAR(255) NOT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  created_by BIGINT UNSIGNED NULL,
  updated_by BIGINT UNSIGNED NULL,
  PRIMARY KEY (id_cliente),
  UNIQUE KEY uk_cliente_id_identificacion (id_identificacion),
  KEY idx_cliente_estado_cliente (estado_cliente_id),
  KEY idx_cliente_tipo_cliente (tipo_cliente_id),
  CONSTRAINT fk_cliente_tipo_cliente FOREIGN KEY (tipo_cliente_id) REFERENCES cat_tipo_cliente (id_catalogo),
  CONSTRAINT fk_cliente_estado_cliente FOREIGN KEY (estado_cliente_id) REFERENCES cat_estado_cliente (id_catalogo)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS cliente_persona (
  id_cliente BIGINT UNSIGNED NOT NULL,
  nombres VARCHAR(120) NOT NULL,
  apellidos VARCHAR(120) NOT NULL,
  fecha_nacimiento DATE NOT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  created_by BIGINT UNSIGNED NULL,
  updated_by BIGINT UNSIGNED NULL,
  PRIMARY KEY (id_cliente),
  CONSTRAINT fk_cliente_persona_cliente FOREIGN KEY (id_cliente) REFERENCES cliente (id_cliente)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS cliente_empresa (
  id_cliente BIGINT UNSIGNED NOT NULL,
  razon_social VARCHAR(200) NOT NULL,
  nit VARCHAR(30) NOT NULL,
  id_representante_legal BIGINT UNSIGNED NOT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  created_by BIGINT UNSIGNED NULL,
  updated_by BIGINT UNSIGNED NULL,
  PRIMARY KEY (id_cliente),
  UNIQUE KEY uk_cliente_empresa_nit (nit),
  KEY idx_cliente_empresa_representante_legal (id_representante_legal),
  CONSTRAINT fk_cliente_empresa_cliente FOREIGN KEY (id_cliente) REFERENCES cliente (id_cliente),
  CONSTRAINT fk_cliente_empresa_representante FOREIGN KEY (id_representante_legal) REFERENCES cliente_persona (id_cliente)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

