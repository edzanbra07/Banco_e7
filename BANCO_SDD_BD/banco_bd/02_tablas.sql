CREATE TABLE IF NOT EXISTS cuenta (
  id_cuenta BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  numero_cuenta VARCHAR(30) NOT NULL,
  id_titular_cliente BIGINT UNSIGNED NOT NULL,
  tipo_cuenta_id BIGINT UNSIGNED NOT NULL,
  moneda_id BIGINT UNSIGNED NOT NULL,
  estado_cuenta_id BIGINT UNSIGNED NOT NULL,
